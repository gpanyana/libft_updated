#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*str;

	str = s;
	if (n == 0)
		return ;
	i = 0;
	while (i < n)
	{
		str[i++] = 0;
	}
	return ;
}
