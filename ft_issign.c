#include "libft.h"

int		ft_issign(int n)
{
	int		i;
	char	*str;

	i = 0;
	str = NULL;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			n = -1;
	return (n);
}
